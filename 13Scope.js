// 'var': Function-scoped variable declaration. It can be called anywhere within the function,
// but it is hoisted to the top of its function scope during execution.

/*'let': Block-scoped variable declaration introduced in ECMAScript 6 (ES6). 
It is limited to the block, statement, or expression where it is defined. Unlike 'var'
 it is not hoisted to the top of the block.*/


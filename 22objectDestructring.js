/*
  - Destructuring assignment is a concise way to extract values from objects and arrays.
  - In the example below, we use destructuring to create variables 'company', 'model', and 'price' directly from the 'product' object.

  let product = {
      company: "Apple",
      model: "13Pro",
      price: 122222
  };

  let { company } = product;
  console.log(company); // Output: "Apple"

  let { model, price } = product;
  console.log(model); // Output: "13Pro"
  console.log(price); // Output: 122222

  - This syntax provides a more concise way of extracting and using individual properties from objects.
*/

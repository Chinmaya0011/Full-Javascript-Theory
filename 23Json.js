/*
  - JSON (JavaScript Object Notation):
    - JSON and objects are similar, but there are key differences.
    - In JSON, keys must be enclosed in double quotation marks.

  // JSON.parse - Convert JSON to object
  let student = {
      "name": "chinu",
      "rollNo": "121203",
      "rating": "4.3"
  };
  console.log(typeof student);

  // JSON.stringify - Convert object to JSON
  let str = JSON.stringify(student);
  console.log(typeof str);

  - JSON is a data format, while a JavaScript object is a data structure.
  - Strings are commonly used in JSON and require double quotes, escaped as \".
  - JSON is often used for data interchange, especially over networks, due to its simplicity and ease of parsing.
*/
